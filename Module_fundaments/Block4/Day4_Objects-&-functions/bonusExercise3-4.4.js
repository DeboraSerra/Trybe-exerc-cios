function yourBasket(basket) {
  let bask = {};
  let yourBask = [];

  for (let value of basket){
    if (bask[value] == undefined) {
      bask[value] = 1;
    } else {
      bask[value] += 1;
    }
  }
  
  for (let key in bask) {
    yourBask.push(bask[key] + ' ' + key);
  }
  console.log('A sua cesta possui:', yourBask.join(','));
}

